# Write a while loop that prints the number 9000 ten times.
count = 0
while count < 10
  puts 9000
  count = count + 1
end

# Write a while loop that prints the even numbers from 2 to 40.

numbers = 2
while numbers <= 40
  puts numbers
  numbers = numbers + 2
end

# Write a while loop that prints the word "hello" 5 times.

count = 0
while count < 5
  puts "hello"
  count = count + 1
end